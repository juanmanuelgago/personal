    /**
     * Encargado de subir la informacion correspondiente a los 
     * jComboBox del frame 
     */
    
    public void cargarDatos() {
        jComboBoxOrigen.removeAllItems();
        jComboBoxDestino.removeAllItems();
        for (TVertice v : vertices) {
            if (!v.getEtiqueta().equals("")) {
                jComboBoxOrigen.addItem((String) v.getEtiqueta());
                jComboBoxDestino.addItem((String) v.getEtiqueta());
            }
        }
    }

