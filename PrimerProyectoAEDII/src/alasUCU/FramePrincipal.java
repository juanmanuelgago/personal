    /**
     * Metodos a realizarse una vez que el boton fue apretado
     * @param evt 
     */
    
    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarActionPerformed
        String origen = jComboBoxOrigen.getSelectedItem().toString();
        String destino = jComboBoxDestino.getSelectedItem().toString();
        Date fecha = jDateChooserFecha.getDate();
        if (origen != null && destino != null && fecha != null) {
            try {
                JTextAreaPrincipal.setText("");
                jTextAreaVueloMasBarato.setText("");
                jTextAreaVueloMasCorto.setText("");
                TCaminos cam = gd.todosLosCaminos(origen, destino, fecha);
                TCamino camBarato = cam.caminoMasBarato();
                TCamino camRapido = cam.menosTiempoDeVuelo();
                for (TCamino camino : cam.getCaminos()) {
                    TVertice o = gd.buscarVertice(camino.getOrigen().getEtiqueta());
                    TVertice d = gd.buscarVertice(camino.getUltimaEtiqueta());
                    JTextAreaPrincipal.append("ORIGEN: " + o.getDatos().getNombre_Aero() + " (" + origen + ")" + "\n");
                    JTextAreaPrincipal.append("DESTINO: " + d.getDatos().getNombre_Aero() + " (" + destino + ")" + "\n");
                    JTextAreaPrincipal.append("CAMINO COMPLETO: " + camino.imprimirEtiquetas() + "\n");
                    JTextAreaPrincipal.append("COSTO: " + camino.getCostoTotal() + "\n");
                    JTextAreaPrincipal.append("--------" + "\n");
                }
                jTextAreaVueloMasBarato.append(camBarato.imprimirEtiquetas() + " COSTO -> " + camBarato.getCostoTotal());
                jTextAreaVueloMasCorto.append(camRapido.imprimirEtiquetas() + " TIEMPO -> " + camRapido.getTiempoTotalVuelo());
            }
            catch (NullPointerException exc) {
                JOptionPane.showMessageDialog(null, "No hay conexiones entre los aeropuertos para ese dia");
            }
        }

    }//GEN-LAST:event_jButtonActualizarActionPerformed

